set(handles.edit11,'String',lst(1,5));  
if mx==lst(1,6) set(handles.edit12,'ForegroundColor','green'); end
set(handles.edit12,'String',lst(1,6));  
if mx==lst(1,7) set(handles.edit13,'ForegroundColor','green'); end
set(handles.edit13,'String',lst(1,7));  
if mx==lst(1,8) set(handles.edit14,'ForegroundColor','green'); end
set(handles.edit14,'String',lst(1,8));  
if mx==lst(1,9) set(handles.edit15,'ForegroundColor','green'); end
set(handles.edit15,'String',lst(1,9));  
if mx==lst(1,10) set(handles.edit16,'ForegroundColor','green'); end
set(handles.edit16,'String',lst(1,10));  
if mx==lst(1,11) set(handles.edit17,'ForegroundColor','green'); end
set(handles.edit17,'String',lst(1,11));  
if mx==lst(1,1) set(handles.edit33,'ForegroundColor','green'); end
